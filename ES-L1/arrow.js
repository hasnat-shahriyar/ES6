// Arrow function, multiple parameter, function body
const add = function(first, second){
    return first + second;
}
// Arrow Function
const add2 = (first, second) => first + second;

const result = add(5, 10);
const result2 = add2(10, 20);
console.log (result);
console.log (result2);

